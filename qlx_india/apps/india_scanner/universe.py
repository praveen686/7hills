def get_fno_symbols(exclude_index: bool = True) -> list[str]:
    """Return sorted list of F&O stock symbols."""
    symbols = list(FNO_UNIVERSE.keys())
    if exclude_index:
        symbols = [s for s in symbols if s not in ("NIFTY", "BANKNIFTY")]
    return sorted(symbols)


def get_lot_size(symbol: str) -> int:
    """Get lot size for a symbol, default 1 if unknown."""
    return FNO_UNIVERSE.get(symbol, 1)
